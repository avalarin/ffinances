  'JP' => [ 'JPY','EUR','USD' ]
}

cc_dict.each_pair do |country_code, currencies|
  country = Country.find_by_code country_code
  raise "Country #{country_code} not found." unless country
  currencies.each do |currency_code|
    currency = Currency.find_by_code currency_code
    puts "Currency #{currency_code} not found." unless currency
    country.currencies.push currency if currency
  end
  country.save!
end